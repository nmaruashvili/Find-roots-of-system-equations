
#include <iostream>
using namespace std;
double D[10][10],Dx[10][10],Dy[10][10],Dz[10][10],detX,detY,detZ,det;
int i,j;
main() {
	cout << "sheikvanet gantolebata sistemis koepicientebi:\n";
	for (i = 1; i <= 3; i ++ ) {
		for( j = 1; j <= 4; j ++) {
			cin >> D[i][j];
		}
	}
	for (i = 1; i <= 3; i ++ ) {
		for( j = 1; j <= 3; j ++) {
			Dx[i][j] = D[i][j];
			Dy[i][j] = D[i][j];
			Dz[i][j] = D[i][j];
		}
	}	
	Dx[1][1] = D[1][4];		Dy[1][2] = D[1][4];		Dz[1][3] = D[1][4]; 	Dx[1][4] = Dx[1][1];
	Dx[2][1] = D[2][4];		Dy[2][2] = D[2][4];		Dz[2][3] = D[2][4];		Dx[2][4] = Dx[2][1];
	Dx[3][1] = D[3][4];		Dy[3][2] = D[3][4];		Dz[3][3] = D[3][4];		Dx[3][4] = Dx[3][1];
	
	Dx[1][5] = Dx[1][2];	Dy[1][4] = Dy[1][1];	Dy[1][5] = Dy[1][2];	Dz[1][4] = Dz[1][1];
	Dx[2][5] = Dx[2][2];	Dy[2][4] = Dy[2][1];	Dy[2][5] = Dy[2][2];	Dz[2][4] = Dz[2][1];
	Dx[3][5] = Dx[3][2];	Dy[3][4] = Dy[3][1];	Dy[3][5] = Dy[3][2];	Dz[3][4] = Dz[3][1];
	
	Dz[1][5] = Dz[1][2];	D[1][4] = D[1][1];		D[1][5] = D[1][2];
	Dz[2][5] = Dz[2][2];	D[2][4] = D[2][1];		D[2][5] = D[2][2];
	Dz[3][5] = Dz[3][2];	D[3][4] = D[3][1];		D[3][5] = D[3][2];
	
	detX = Dx[1][1] * Dx[2][2] * Dx[3][3] + Dx[1][2] * Dx[2][3] * Dx[3][4] + Dx[1][3] * Dx[2][4] * Dx[3][5] -
		   Dx[1][3] * Dx[2][2] * Dx[3][1] - Dx[1][4] * Dx[2][3] * Dx[3][2] -Dx[1][5] * Dx[2][4] * Dx[3][3];
		   
	detY = Dy[1][1] * Dy[2][2] * Dy[3][3] + Dy[1][2] * Dy[2][3] * Dy[3][4] + Dy[1][3] * Dy[2][4] * Dy[3][5] -
		   Dy[1][3] * Dy[2][2] * Dy[3][1] - Dy[1][4] * Dy[2][3] * Dy[3][2] - Dy[1][5] * Dy[2][4] * Dy[3][3];
		   
	detZ = Dz[1][1] * Dz[2][2] * Dz[3][3] + Dz[1][2] * Dz[2][3] * Dz[3][4] + Dz[1][3] * Dz[2][4] * Dz[3][5] -
		   Dz[1][3] * Dz[2][2] * Dz[3][1] - Dz[1][4] * Dz[2][3] * Dz[3][2] -Dz[1][5] * Dz[2][4] * Dz[3][3];
		   
	det = D[1][1] * D[2][2] * D[3][3] + D[1][2] * D[2][3] * D[3][4] + D[1][3] * D[2][4] * D[3][5] -
		  D[1][3] * D[2][2] * D[3][1] - D[1][4] * D[2][3] * D[3][2] - D[1][5] * D[2][4] * D[3][3];
	cout << "X1 = " << detX/det << endl << "X2 = " << detY/det << endl << "X3 = " << detZ/det;
}
